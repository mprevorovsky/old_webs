<p>Jmenuji se <b>Lenka Převorovská</b>.</p>
<p>V roce 2004 jsem maturovala na Gymnáziu Jana Keplera v Praze. Po pěti letech studia sociologie (zaměření Sociální konstrukce identit) jsem promovala na Fakultě sociálních věd Univerzity Karlovy v Praze a získala titul Magistry sociálních věd. Už v průběhu studia se ale stále zřetelněji rýsovala moje další cesta – stále více mě fascinovalo lidské tělo a skryté souvislosti mezi jednotlivými jeho částmi, což vedlo k silné potřebě dozvědět se o něm více. Proto jsem v roce 2009 začala studovat obor fyzioterapie na 2. lékařské fakultě UK v Motole, který studuji dosud.</p>
<p>Delší dobu jsem masírovala intuitivně, moje masérská činnost dostala řád až s příchodem mé učitelky Evy Juřenové. Znalosti jsem si pak dále doplnila a certifikát MZ ČR získala v masérské škole <a href="http://www.maserske-kurzy.cz/" target="_blank" title="Masérské kurzy EduSpa College">EduSpa College</a>.</p>
<p>V roce 2006 jsem absolvovala kurz prvního stupně <a href="http://www.reiki-centrumpraha.cz/" target="_blank" title="Reiki centrum Praha">Reiki</a>.</p>
<p>V roce 2008 jsem nastoupila cestu intenzivního sebepoznávání a pochopila jsem propojení mezi tělem a myslí. Začátkem roku 2011 jsem absolvovala základní kurz EFT. Ve své praxi bych chtěla postupně rozšiřovat sortiment svých služeb tak, aby lidé se zájmem o zlepšení svého zdravotního stavu a kvality života, stejně jako lidé hledající odpovědi na své otázky, mohli s mým přispěním pomoci sami sobě.</p>
<p>Mezi mé zájmy patří hledání odpovědí na otázky, studium, tajči (Čchen Tchaj-ťi čchüan, aplikovaná metoda), bylinářství, drahé kameny, kreslení pastelkami, hudba, tanec a zpěv, cestování, projektování zahrad podle pravidel <a href="http://ekozahrady.com/" target="_blank" title="Ekozahrady Jaroslava Svobody">permakultury</a> a další tvořivé aktivity, například vaření.</p>


